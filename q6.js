// Perform arithmetic operations on two numbers

let operator = "*";
let num1 = 5, num2 = 5;

if(operator == "+"){
    console.log(num1 + num2);
}

else if(operator == "-"){
    console.log(num1 - num2);
}

else if(operator == "*"){
    console.log(num1 * num2);
}

else if(operator == "/"){
    console.log(num1 / num2);
}

else{
    console.log("Invalid operation");
}